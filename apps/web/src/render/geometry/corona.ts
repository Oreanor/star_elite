import { PlaneGeometry } from 'three'

/**
 * Геометрия короны — единичный билборд (плоскость 1×1), который вызывающий
 * разворачивает лицом к камере и масштабирует до размера ореола.
 *
 * Свечение вокруг звезды нельзя сделать мешем-оболочкой: у него нет поверхности, это
 * рассеяние света. Раньше корона была радиальным градиентом на спрайте — ровный круг,
 * «лампочка». Теперь на этой плоскости живёт процедурный шейдер (см. materials/starCorona):
 * плазма течёт, край колышется, из лимба бьют жилы. Плоскость — одна на все звёзды.
 */

let geometry: PlaneGeometry | null = null

export function coronaGeometry(): PlaneGeometry {
  if (!geometry) geometry = new PlaneGeometry(1, 1)
  return geometry
}
