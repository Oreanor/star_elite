import { AdditiveBlending, Color, DoubleSide, ShaderMaterial } from 'three'
import { CORONA } from '../config'

/**
 * Корона звезды — процедурная, а не картинка.
 *
 * Прежняя корона была статичным радиальным градиентом на спрайте: ровный круг,
 * «лампочка». Настоящая корона живёт — плазма течёт, из лимба бьют протуберанцы,
 * край колышется. Здесь это считает фрагментный шейдер на билборде.
 *
 * ГЛАВНОЕ ограничение геометрии: билборд стоит в ЦЕНТРЕ звезды, и её диск закрывает
 * внутреннюю часть плоскости — ровно долю `1/CORONA.SCALE` её радиуса. Вся корона
 * ОБЯЗАНА жить СНАРУЖИ этого круга, иначе яркость и жилы прячутся за диском, а наружу
 * торчит бледная кайма — «рассеянное свечение без шейдера». Поэтому радиус нормируем
 * от КРАЯ ДИСКА (лимба) к краю плоскости: `uDiskFrac` двигает начало короны на лимб,
 * жилы-стримеры бьют оттуда наружу, протуберанцы горят у самого лимба.
 *
 * Билборд разворачивает вызывающий (см. Bodies): плоскость смотрит в камеру, как спрайт.
 * Аддитивная и без записи глубины — свечение складывается с фоном; глубину ПРОВЕРЯЕТ,
 * чтобы шар звезды (и планета перед ней) закрыл дальнюю половину ореола честно.
 */

const vertex = /* glsl */ `
#include <common>
#include <logdepthbuf_pars_vertex>

varying vec2 vUv;

void main() {
  vUv = uv;
  gl_Position = projectionMatrix * modelViewMatrix * vec4(position, 1.0);
  #include <logdepthbuf_vertex>
}
`

const fragment = /* glsl */ `
#include <common>
#include <logdepthbuf_pars_fragment>

uniform float uTime;
uniform vec3 uColor;
/** Доля радиуса плоскости, занятая ДИСКОМ звезды: за ним короны не видно, там она начинается. */
uniform float uDiskFrac;

varying vec2 vUv;

float hash(vec2 p) {
  p = fract(p * vec2(123.34, 456.21));
  p += dot(p, p + 45.32);
  return fract(p.x * p.y);
}

float noise(vec2 p) {
  vec2 i = floor(p);
  vec2 f = fract(p);
  f = f * f * (3.0 - 2.0 * f);
  return mix(
    mix(hash(i), hash(i + vec2(1.0, 0.0)), f.x),
    mix(hash(i + vec2(0.0, 1.0)), hash(i + vec2(1.0, 1.0)), f.x),
    f.y
  );
}

float fbm(vec2 p) {
  float sum = 0.0;
  float amp = 0.5;
  for (int i = 0; i < 5; i++) {
    sum += amp * noise(p);
    p *= 2.03;
    amp *= 0.5;
  }
  return sum;
}

void main() {
  #include <logdepthbuf_fragment>

  vec2 p = vUv - 0.5;
  float r = length(p) * 2.0;     // 0 в центре, 1 у кромки билборда
  if (r > 1.0) discard;          // billboard круглый, а не квадратный

  // Радиус ОТ КРАЯ ДИСКА к кромке плоскости: rr=0 на лимбе, rr=1 у края короны. Вся
  // видимая корона живёт в rr∈[0,1]; область самого диска (rr<0) отдаём шару звезды.
  float rr = (r - uDiskFrac) / (1.0 - uDiskFrac);
  if (rr < 0.0) discard;

  float ang = atan(p.y, p.x);

  // РЯБЬ по УГЛУ: раньше язык сидел строго на сетке — ровный частокол, «ирокез».
  // Двумя октавами шума ВОЛНИМ сам угол перед нарезкой на языки: сетка изгибается
  // сильно и неравномерно, протуберанцы гуляют, как течение плазмы. Ползёт во времени —
  // тот же приём доменного варпа, что рябит кромку пузыря у чёрной дыры, только в 2D.
  float wobble = (fbm(vec2(ang * 2.2 + uTime * 0.05, rr * 0.9)) - 0.5)
               + 0.5 * (fbm(vec2(ang * 5.0 - uTime * 0.07, rr * 1.6)) - 0.5);
  float angW = ang + wobble * 1.7;

  // Стримеры: чаще (angW*8 — под два десятка языков) и детально по волнистому УГЛУ, тянутся
  // по радиусу наружу — жилы, а не пятна. Домен искажаем вторым fbm, чтобы течение не
  // читалось циклом; ползёт во времени.
  vec2 polar = vec2(angW * 8.0, rr * 2.0 - uTime * 0.16);
  float s = fbm(polar + fbm(polar * 0.6 + uTime * 0.05));
  // РЕМАП в контраст: сырой fbm держится в среднем ~0.5 и даёт ровную кашу, из-за чего
  // корона и выходила гладким кольцом без протуберанцев. Порог + степень оставляют
  // отдельные ЯЗЫКИ (яркие пики), а между ними — провалы. Это и есть искажение.
  float streamers = pow(smoothstep(0.32, 0.85, s), 1.5);

  // Докуда добивает жила под этим углом. Слабая база (языков нет — почти нет короны),
  // сильный ход от стримеров: там где язык — достаёт заметно дальше от лимба. Ход поднят
  // (0.70 → 1.05): протуберанцы вытягиваются в полтора раза дальше от лимба.
  float reach = 0.14 + 1.05 * streamers;
  float tongue = pow(smoothstep(reach, 0.0, rr), 1.3);

  // ВОРОТНИК у самого лимба прячет гранёную кромку шара — но сам МОДУЛИРОВАН углом:
  // база кроет силуэт по всему кругу, а надбавка от стримеров зажигает над ним
  // яркие протуберанцы. Так круг ломается, но дыр в укрытии кромки не появляется.
  float collar = pow(smoothstep(0.16, 0.0, rr), 0.9);
  // База невысокая: аддитив с высокой базой упирается в белый по всему кругу и снова
  // стирает языки. Пусть кромку кроет мягко, а яркость несут пики стримеров.
  float collarLit = collar * (0.6 + 2.1 * streamers);

  // ВЕНЧИК — плотная РАВНОМЕРНАЯ по углу полоса у самого лимба. Теперь, когда корона
  // выведена на видимую кромку (uDiskFrac), венчик реально виден и делает главную работу:
  // засвечивает и «размазывает» гранёный силуэт шара — грани тонут в сплошном ярком ободе,
  // круг читается ровным. Ширим до rr≈0.14 (нахлёст под шаром гасится, видимая часть яркая),
  // спад плавный — светящийся аутлайн, а не резкое кольцо.
  float rim = pow(smoothstep(0.16, 0.0, rr), 0.7);

  float intensity = rim * 2.6 + collarLit * 1.6 + tongue * 1.7;

  // Добела на ЯЗЫКАХ у лимба (перегретая плазма), в провалах и наружу — в цвет класса.
  // Белим по стримерам, а не только по радиусу: протуберанец — раскалённый, тело — цветное.
  vec3 col = mix(uColor, vec3(1.0), smoothstep(0.12, 0.0, rr) * (0.35 + 0.5 * streamers));
  gl_FragColor = vec4(col * intensity * 2.6, 1.0);
}
`

/**
 * Материал короны одной звезды. Свой на звезду: `uColor` от класса, `uTime` двигает сцена.
 */
export function createCoronaMaterial(color: number): ShaderMaterial {
  return new ShaderMaterial({
    vertexShader: vertex,
    fragmentShader: fragment,
    uniforms: {
      uTime: { value: 0 },
      uColor: { value: new Color(color) },
      // ГДЕ на билборде кромка шара. Плоскость короны — PlaneGeometry(1,1), масштаб glowSize=
      // radius·SCALE, значит её ПОЛУразмер = 0.5·SCALE·radius, а шар радиуса radius занимает
      // r = radius/(0.5·SCALE·radius) = 2/SCALE билборда. Прежние 0.9/SCALE сажали корону
      // ВДВОЕ ближе к центру — весь плотный воротник и венчик уходили ПОД шар и гасились им
      // по глубине, наружу торчали лишь кончики жил (тот самый «ирокез»). Ставим кромку на
      // 2/SCALE, чуть внутрь (×0.975) для нахлёста без тёмной каймы: теперь плотная часть
      // короны горит РОВНО на видимом лимбе и кроет гранёный силуэт.
      uDiskFrac: { value: (2.0 * 0.975) / CORONA.SCALE },
    },
    transparent: true,
    blending: AdditiveBlending,
    depthWrite: false,
    side: DoubleSide,
    fog: false,
  })
}
