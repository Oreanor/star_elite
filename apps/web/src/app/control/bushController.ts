import type { Controller, ShipEntity, World } from '@elite/sim'

/**
 * Штурвал на КУСТЕ галактик: манёвров нет, есть только газ.
 *
 * Между галактиками рулить не по чему — корабль едет по ребру, как вагонетка. Свободный
 * полёт превратил бы куст в обычное трёхмерное поле и убил бы всю его геометрию: ветки
 * можно было бы срезать, а выбор в узле перестал бы быть выбором.
 *
 * Это ОБЫЧНЫЙ `Controller` — тот же шов, что у автостыковки: он лишь заполняет
 * `ShipControls`, ничего не зная ни о рельсах, ни о физике. Сам ход вдоль ребра считает
 * домен (`stepBushTravel`), а сюда попадает только «газ дан или нет» — чтобы поза корабля
 * в кадре не воевала с положением на рельсе.
 *
 * Тяга обнуляется вся: даже она не двигает корабль в обычном смысле, потому что позу
 * задаёт рельс. Оставляем `throttle` только как СИГНАЛ «еду», его читает шаг рельсов.
 */
export const bushController: Controller = {
  update(ship: ShipEntity, _world: World, _dt: number): void {
    const c = ship.controls
    // Всё, чем пилот обычно вертит бортом, молчит: pitch/yaw/roll/руль/стрейфы.
    c.pitch = 0
    c.yaw = 0
    c.roll = 0
    c.rudder = 0
    c.strafe = 0
    c.strafeUp = 0
    // Ни тормоза, ни форсажа: ход по ребру задан долей ребра в секунду, и разгонять
    // его нечем — иначе узел проскакивался бы, а он есть выбор.
    c.retro = 0
    c.boost = 1
    c.cruise = 1
    c.flightAssist = true
  },

  wantsFire(): boolean {
    // Стрелять по галактикам не во что, а случайный выстрел на рельсах — только шум.
    return false
  },
}
