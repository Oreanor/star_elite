import { ref, remove } from 'firebase/database'
import { rtdb, serverNow } from './firebase'

/**
 * УБОРКА МЕРТВЕЦОВ. Присутствие и позы держатся на `onDisconnect`: закрыл вкладку — RTDB
 * стёрла узел сама. Это БЫСТРЫЙ путь, но не гарантия: сервер может не заметить обрыв, а
 * сборка, писавшая узел, могла и вовсе не вешать onDisconnect. Такой узел не стирает уже
 * никто и никогда — он висит в базе вечно, и игрок, вышедший месяц назад, остаётся «в сети».
 *
 * Поэтому у записи есть СРОК ГОДНОСТИ: живой клиент подтверждает жизнь, переписывая свою
 * отметку `t` (presence — раз в 2 с, поза — 12–15 раз в секунду). Мертвец её не переписывает
 * никогда, и по этому его и видно.
 *
 * Уборщик — любой, кто мертвеца увидел: своего сервера у нас нет, cron-у взяться неоткуда,
 * а клиентов много. Правила базы разрешают удалить ЧУЖОЙ узел лишь при `t` старше
 * `REAP_AFTER_MS` — живого не тронешь, даже подделав часы: `now` в правилах серверное.
 */

/**
 * Мертвее этого — узел вычищается из базы, мс. Порог ЩЕДРЫЙ и намеренно много больше срока
 * показа: вычёркивать из списка можно поспешно (ошибся — вернётся через пару секунд), а
 * СТИРАТЬ чужое — только наверняка. Фоновая вкладка душит таймеры до раза в минуту, поэтому
 * даже задремавший клиент подтверждает жизнь много чаще этого срока.
 */
export const REAP_AFTER_MS = 300_000

/**
 * Мёртв ли узел с такой отметкой. Отметки НЕТ вовсе — тоже мёртв: её пишут все нынешние
 * сборки, а значит узел без неё оставлен прежней и подтвердить жизнь уже не может никогда.
 * Ровно такие и висели в стартовой системе.
 */
export function deadStamp(t: unknown): boolean {
  if (typeof t !== 'number') return true
  return serverNow() - t > REAP_AFTER_MS
}

/** Стереть мёртвый узел. Отказ правил — не беда: уберёт следующий, кто его увидит. */
export function reapDead(path: string): void {
  if (!rtdb) return
  void remove(ref(rtdb, path)).catch(() => {})
}
