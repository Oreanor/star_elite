import { Vector3 } from 'three'
import type { Rng } from '../../core/math'
import type { Task } from './tasks'

export type AIMode = 'patrol' | 'pursue' | 'attack' | 'break' | 'evade'

/**
 * Фаза стыковки трафика. null — корабль не стыкуется вовсе.
 *   inbound  — заходит на причал (или ждёт очередь, если он занят);
 *   berthed  — стоит у причала положенное время;
 *   done     — отчалил и уходит прочь, дальше живёт обычным трафиком.
 */
export type DockPhase = 'inbound' | 'berthed' | 'done' | null

/**
 * Память пилота-бота. Он не двигает корабль — он заполняет тот же ShipControls,
 * что и игрок мышью. Всё, что он умеет, физически доступно и тебе.
 */
export interface AIState {
  mode: AIMode
  /** Кого атакуем. null — цели нет. Выбирается по фракции, а не «это всегда игрок»:
   *  иначе полиция никогда не подерётся с пиратами. */
  targetId: number | null
  /**
   * ПРИКАЗ: атаковать именно этого и никого другого. null — пилот выбирает сам.
   *
   * Так работает автобой на корабле игрока: тот же пилот, та же физика, но цель
   * ему назначена, а не выбрана. Без приказа он в такте размышления переключился бы
   * на ближайшего врага, и «автобой по захваченной цели» перестал бы означать то,
   * что написано на кнопке.
   */
  orderedTargetId: number | null
  /** Куда сейчас летим: патрульная точка или точка отрыва. */
  waypoint: Vector3
  /** Район патрулирования. Бот не улетает из него без причины. */
  home: Vector3

  /** До следующего пересмотра решения. Это и есть время реакции. */
  thinkTimer: number
  /** Сколько бот уже в текущем режиме. */
  modeTimer: number

  /** Личный сдвиг фазы: без него звено маневрирует синхронно, как балет. */
  phase: number

  /** Смещение точки прицеливания — «рука дрожит». */
  aimJitter: Vector3
  aimJitterTimer: number

  wantsFire: boolean
  /** Решение о пуске принимается в такте размышления, а не каждый шаг физики. */
  wantsMissile: boolean
  /** Секунд до следующего возможного пуска. Иначе бот высыпает всю пусковую разом. */
  missileCooldown: number
  /** Жать ли ПРО. Тоже решение такта размышления: бот видит ракету не мгновенно. */
  wantsEcm: boolean

  /**
   * Кого сопровождает. null — никого.
   *
   * Наёмник держится рядом с нанимателем и бьёт того, кого тот захватил. Своей
   * цели он не выбирает: он не второй пилот, а ведомый.
   */
  escortOf: number | null
  /**
   * Выучка, 0..1. Единица — обычный пилот.
   *
   * Слабость моделируется ЧЕСТНО: медленнее реакция, шире разброс. Урон и
   * угловая скорость у наёмника те же, что у всех, — он проигрывает решением,
   * а не заниженным оружием. Ровно этим правилом живут и пираты.
   */
  skill: number

  /** Стыкуется ли к причалу и на какой стадии. null — обычный трафик. */
  dock: DockPhase
  /** Секунд у причала до отчаливания. Тикает только в фазе `berthed`. */
  dockTimer: number

  /**
   * Дремлет на палубе платформы-гнезда. Пока true — пилот не двигает корабль,
   * не выбирает цель и не стреляет: его можно вырезать по одному, и он не
   * шевельнётся. Будит гнездо `stepPlatforms`, снимая флаг разом со всего звена.
   */
  dormant: boolean

  /** Стоящий приказ игрока СВОЕМУ автоботу. См. `AICommand`. */
  command: AICommand

  /**
   * Очередь ПОРУЧЕНИЙ, взятых в разговоре (см. `tasks.ts`). Пустая — бот действует по
   * приказу/эскорту как обычно; непустая — уходит исполнять её (сбор груза, встреча, позже
   * перелёт/покупка/разговор), голова очереди — текущая задача. Приоритет над боем: послал
   * за делом — он делом и занят, а не висит в свалке.
   */
  tasks: Task[]

  /**
   * Обратный отсчёт зарядки прыжка-побега, с. Отрицательный — не заряжает. Пока
   * тикает, бот уходит от угрозы и уязвим; дошёл до нуля — уходит из системы.
   */
  warpTimer: number

  /**
   * Претензия к игроку за попадания: счётчик засчитанных попаданий подряд, 0 — нет.
   * Пока > 0 и борт ещё не враг — пилот вызывает по связи и ждёт объяснений (это
   * читает UI). На `GRIEVANCE.HOSTILE_HITS` он становится враждебным честно. Гаснет
   * сам через `GRIEVANCE.COOLDOWN` без новых попаданий. Разряд — словом или кнопкой.
   */
  grievance: number
  /** `world.time` последнего засчитанного попадания. Для дебаунса и угасания претензии. */
  grievanceAt: number
  /** Когда началась текущая претензия — для таймера ответного огня. */
  grievanceSince: number
  /** Засчитанные попадания игрока в текущей серии (включая прощённые). */
  strikeCount: number
}

/**
 * Приказ игрока своему автоботу — собственному автобою или нанятому эскорту.
 *
 * Это команды ПОСЛУШАНИЯ, а не уговоры: их отдают напрямую (кнопкой из выпадушки
 * или голосом), потому что бот уже на твоей стороне и решать ему нечего. Уговорить
 * ЧУЖОГО что-то сделать — дело разговора (LLM) или скриптового взвешивания персоны,
 * а не этого поля.
 *
 *   default   — бот действует сам: эскорт бьёт цель, захваченную нанимателем;
 *   attack    — бить/догонять именно `orderedTargetId` (конкретный корабль);
 *   engageAll — свободный огонь по всем враждебным вокруг;
 *   hold      — стоять на месте, в бой не лезть, пока игрок отлучился;
 *   standDown — прекратить огонь, никого не атаковать, держаться рядом;
 *   keepBack  — держаться в хвосте и уходить от боя, беречь себя и груз.
 */
export type AICommand = 'default' | 'attack' | 'engageAll' | 'hold' | 'standDown' | 'keepBack'

export function createAIState(home: Vector3, rng: Rng): AIState {
  return {
    mode: 'patrol',
    targetId: null,
    orderedTargetId: null,
    waypoint: home.clone(),
    home: home.clone(),
    thinkTimer: rng() * 0.12,
    modeTimer: 0,
    phase: rng() * Math.PI * 2,
    aimJitter: new Vector3(),
    aimJitterTimer: 0,
    wantsFire: false,
    wantsMissile: false,
    // Первая ракета не летит в первые же секунды боя: дай игроку осмотреться.
    missileCooldown: 6,
    wantsEcm: false,
    escortOf: null,
    skill: 1,
    dock: null,
    dockTimer: 0,
    dormant: false,
    command: 'default',
    tasks: [],
    warpTimer: -1,
    grievance: 0,
    grievanceAt: -1e9,
    grievanceSince: -1e9,
    strikeCount: 0,
  }
}
