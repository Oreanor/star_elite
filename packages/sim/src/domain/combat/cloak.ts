import type { ShipEntity } from '../world/entities'

/**
 * Маскировочное поле. Корабль перестаёт отражать свет: его не видят ни глаз,
 * ни локатор, ни головка ракеты.
 *
 * Плата — главные батареи, те же, из которых бьёт ПРО. Таймера у поля нет: оно
 * держится ровно столько, сколько хватает электричества, и опадает само, когда
 * не хватило. Отсюда и вся механика — счёт за расход, а не «длительность
 * невидимости», которую пришлось бы балансировать отдельно от батарей.
 *
 * ПОД ПОЛЕМ НЕ СТРЕЛЯЮТ. Излучатели обесточены — вся мощность в поле. Это не
 * штраф, а то, что делает маскировку побегом, а не безнаказанностью: невидимка
 * с работающими стволами убивает всё живое без единого ответного выстрела.
 *
 * Правило видимости живёт здесь, в одном экземпляре (`isVisible`). Захват цели,
 * ИИ и головка ракеты спрашивают ЕГО, а не проверяют флаг у себя: три копии
 * одного условия однажды разойдутся, и невидимка окажется видимой ракете.
 */

/** Есть ли на корабле поле. Ноль расхода — устройства нет, прятаться нечем. */
export const hasCloak = (e: ShipEntity): boolean => e.spec.cloakDrain > 0

/**
 * Видит ли этот корабль кто-то посторонний. Единственный источник правды.
 *
 * Мёртвый невидим тоже: обломок не цель. Это уже было рассеяно по коду
 * проверками `alive`, и здесь они сходятся в одну.
 *
 * ПРИСТЫКОВАННЫЙ невидим по той же причине, что и свой корабль в доке: причал ведёт
 * ВНУТРЬ станции, и стоящий там борт в космосе не висит. Раньше он торчал у ворот всё
 * время стоянки — его метили, захватывали и по нему стреляли, хотя по смыслу его там нет.
 * Отходя (`dock: 'done'`), он появляется снова — это и есть вылет из шлюза.
 */
export const isVisible = (e: ShipEntity): boolean =>
  e.alive && !e.cloaked && e.ai?.dock !== 'berthed'

/**
 * Хватит ли заряда, чтобы поле продержалось хоть сколько-то осмысленно.
 *
 * Секунда — не «мало», а минимум, ниже которого включение было бы обманом:
 * поле мигнуло бы и опало, а батареи опустели.
 */
const MIN_SECONDS = 1

export function canCloak(e: ShipEntity): boolean {
  // Маскировка питается от доп-отсека (аукс) — по нему и проверяем запас.
  return e.alive && hasCloak(e) && e.auxEnergy >= e.spec.cloakDrain * MIN_SECONDS
}

/** Одна клавиша поднимает поле и она же его опускает. @returns новое состояние. */
export function toggleCloak(e: ShipEntity): boolean {
  if (e.cloaked) {
    e.cloaked = false
    return false
  }
  if (!canCloak(e)) return false
  e.cloaked = true
  return true
}

/**
 * Расход поля за шаг. Зовётся каждый шаг для каждого корабля.
 *
 * Доп-отсек копится `regenAux` в том же шаге, поэтому расход должен превышать
 * восполнение — иначе поле держалось бы вечно и было бы не решением, а режимом.
 */
export function stepCloak(e: ShipEntity, dt: number): void {
  if (!e.cloaked) return

  // Мёртвый не прячется: обломок обязан быть виден, иначе трофеи ищут вслепую.
  if (!e.alive) {
    e.cloaked = false
    return
  }

  // Маскировка питается от ДОП-ОТСЕКА (аукс), а не от главной батареи: тот же общий пул,
  // что бомба и ПРО. Оттого под полем не набрать бомбу — размен, а не бесплатный режим.
  e.auxEnergy = Math.max(0, e.auxEnergy - e.spec.cloakDrain * dt)
  if (e.auxEnergy <= 0) e.cloaked = false
}
