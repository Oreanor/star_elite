import type { ShipEntity } from '../world/entities'
import { isVisible } from './cloak'

/**
 * Кого вообще можно взять на прицел.
 *
 * ДВЕ причины не стрелять, и они разные. Замаскированного не видно — его нет ни
 * для глаза, ни для локатора. Идущего на стыковку видно прекрасно, но станция
 * взяла его под защиту, и открывать по нему огонь в её створе никто не станет.
 *
 * Правило одно на всех, кто выбирает цель: ИИ, автобой, головка ракеты. Рендер
 * и локатор спрашивают `isVisible` — им важно, видно ли корабль, а не можно ли
 * по нему бить. Смешать эти два вопроса значило бы сделать стыкующегося
 * невидимкой: он пропал бы с радара ровно тогда, когда за ним интереснее всего
 * следить.
 */
export const isEngageable = (e: ShipEntity): boolean => isVisible(e) && !e.clearance
