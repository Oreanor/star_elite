export interface ShipEntity {
  id: number
  kind: 'ship'
  faction: Faction
  /**
   * Отображаемое имя. До знакомства — общее («Торговец»): незнакомца на радаре и в
   * прицеле не зовут по имени. В миг знакомства (`rememberPilot`) сюда встаёт `pilotName`.
   */
  name: string
  /**
   * Истинное имя ПИЛОТА, выданное при рождении по его виду (землянин — земное, синтет
   * с приставкой, валдри — слоговое). Существует всегда, но игроку открывается лишь при
   * знакомстве; на станции же, где читаешь манифест причала, имена видны сразу.
   */
  pilotName: string

  /** Что установлено. Меняется на станции и при подборе трофеев. */
  loadout: Loadout
  /** Выведено из loadout + массы груза. Пересобирается на СОБЫТИЕ, не каждый кадр. */
  spec: ShipSpec
  /**
   * Разовые прокачки СОБСТВЕННЫХ х-к рамы (HP / грузоподъёмность / аукс) — каждую можно
   * усилить один раз на +25% (см. `deriveShipSpec`/`HullUpgrades`). Сбрасываются при смене
   * корпуса: качаешь именно эту раму. Хранятся в сейве игрока. У ботов — по достатку.
   */
  hullUp: HullUpgrades

  state: ShipState
  controls: ShipControls

  hull: number
  shield: number
  /**
   * Нагрев КОРПУСА от близкой звезды, 0..1. Отдельно от нагрева стволов (тот
   * живёт в `guns[].heat`): корпус греет корона, а не стрельба. За порогом
   * (см. `STAR_HEAT`) начинает течь сначала щит, потом обшивка. Падает сам,
   * стоит отойти, — единственная защита тут манёвр, а не модуль.
   */
  hullHeat: number
  /**
   * Заряд гипердвигателя, световых лет доступной дальности. Это и есть радиус
   * сферы прыжка на карте. Тратится КАЖДЫМ прыжком на пройденную дальность,
   * восполняется только у звезды (см. `HYPERDRIVE`). Потолок — `spec.jumpRange`.
   */
  jumpCharge: number
  /** Время последнего попадания: щит не восстанавливается сразу. */
  lastHitAt: number
  /**
   * Время последнего попадания, которое ПРИНЯЛ ЩИТ, и последнего, дошедшего до КОРПУСА.
   * Порознь ради обратной связи: пока цел щит — на попадании вспыхивает защитная сфера;
   * щит пробит — корпус вздрагивает. Домен лишь метит момент, эффект рисует/трясёт рендер.
   */
  lastShieldHitAt: number
  lastHullHitAt: number
  /** Главная батарея (реактор двигателя): полёт, форсаж, оружие. Копится сама. */
  energy: number
  /** Перезаряд ПРО, с. */
  ecmCooldown: number
  /** Батарея ДОП-ОТСЕКА, ед. Общий запас всех аукс-устройств: бомба, ПРО, маскировка. */
  auxEnergy: number
  /**
   * Маскировочное поле поднято. Корабль не отражает свет и не виден чужим:
   * ни локатору, ни ИИ, ни головке ракеты.
   */
  cloaked: boolean

  hold: CargoHold
  guns: GunState[]
  /** Крейсерский ход: множитель и причина, по которой он не включается. */
  cruise: CruiseState
  /** На какой поверхности стоит корабль; null — свободный полёт. */
  landedOn: SurfaceBinding | null

  alive: boolean
  /** Взрыв уже показан — чтобы не повторять каждый кадр. */
  wreckAt: number | null

  /** Только у ботов. Игрок управляется PlayerController'ом из слоя приложения. */
  ai: AIState | null

  /**
   * Корабль под защитой станции: его не атакуют.
   *
   * Станция даёт коридор тому, кто идёт на стыковку, и стрельба в её створе
   * прекращается. Это правило МИРА, а не поблажка автопилоту: флаг живёт на
   * корабле, и однажды его получит не только игрок.
   */
  clearance: boolean

  /**
   * Чей это беспилотник. `null` — настоящий корабль.
   *
   * Поле нужно не физике, а бухгалтерии: аппарат не даёт очков, не оставляет
   * трофеев и считается против потолка одновременных у своего носителя.
   */
  droneOf: number | null
  /** Момент самоликвидации по `world.time`. `null` — живёт, пока не собьют. */
  dieAt: number | null

  /** Личность пилота: нрав и черты для торга. Данные, не логика. */
  persona: Persona

  /** id записи в реестре знакомств, если игрок уже говорил с этим пилотом. null — незнакомец. */
  acquaintanceId: number | null
  /** Каким типом встречи рождён — чтобы воссоздать сборку при повторной встрече. null — не из трафика. */
  originKind: string | null

  /**
   * Ушёл из системы гиперпрыжком. Не гибель: ни взрыва, ни трофеев, ни награды —
   * его просто больше нет здесь. Уборка снимает такой борт молча, отдельно от
   * убитых, а на его месте `jumpOut` оставляет вспышку перехода.
   */
  warpedOut: boolean
  /** Вылетает из гиперпортала — ИИ и стрельба молчат, пока не притормозит. */
  warpEmerging: boolean
  /** Уходит в гиперпортал — разгон к кольцу и исчезновение в «дыре». */
  warpDeparting: boolean

  /**
   * Кинематический борт: его состояние ЗАДАЁТСЯ ИЗВНЕ каждый кадр, а не выводится
   * интегратором из сил. Симуляция такой борт НЕ рулит и НЕ двигает: контроллер,
   * физика и столкновения его пропускают — он для них подвижная цель/препятствие,
   * чьё положение уже известно. Оружие по нему работает как по любому кораблю (его
   * ищет `castLaser`/наведение), но локально он не стреляет — залпы приходят извне.
   *
   * Это чистое физическое понятие (предписанное движение), а не «сетевое»: домен не
   * знает, ОТКУДА берётся состояние. На клиенте так живёт чужой игрок — его позу
   * ставит интерполятор по снапшотам; `undefined`/false — обычный борт.
   */
  kinematic?: boolean
}

