/**
 * Лазерный болт — снаряд, а не мгновенный луч. Летит по прямой с постоянной
 * скоростью (`GUNNERY.BOLT_SPEED`) и проверяет попадание ЗАМЕТАНИЕМ отрезка за шаг:
 * за 1/120 с болт проходит ~200 м, поэтому точечная проверка «где болт сейчас»
 * пролетала бы сквозь двадцатиметровый корабль между шагами. Каждый шаг он тестирует
 * весь пройденный отрезок тем же `castLaser`, что раньше стрелял на всю дальность.
 *
 * Существует в домене, а не в рендере: попадание — событие симуляции (урон, обида,
 * скол астероида), и по сети синхронизируется именно оно, а не картинка. Визуальный
 * след болт оставляет сам, отдавая короткий `Tracer` за каждый пройденный шаг.
 */
export interface BoltEntity {
  id: number
  kind: 'bolt'
  pos: Vector3
  /** Скорость, м/с: направление × `BOLT_SPEED`. Длина постоянна за всё время жизни. */
  vel: Vector3
  /** Кто выстрелил. По нему болт не бьёт в стрелка и метит попадания для обиды. */
  ownerId: number
  /** Выстрел врага (для цвета следа) — иначе выстрел игрока (тогда попадание копит претензию). */
  hostile: boolean
  /** Выпущен из-под маскировки: бьёт только спящее гнездо, как и мгновенный луч раньше. */
  cloaked: boolean
  /** Урон при попадании. Живёт на болте, а не на стрелке: стрелок мог погибнуть в полёте. */
  damage: number
  /** id лазера, из которого выпущен, — рендер красит след по нему. Домен цветов не знает. */
  weapon: string
  /** Сколько ещё метров болт может пролететь до угасания. Убывает каждый шаг. */
  distanceLeft: number
  born: number
  alive: boolean
}

/**
 * Обращение спутника вокруг своей планеты.
 *
 * Положение НЕ накапливается по кадрам: оно считается из общих физических секунд как
 * `phase + rate·t`. Накопление зависит от частоты шага, не переживает паузу и
 * расходится на двух машинах — а луна обязана висеть там же и у сервера.
 */
export interface OrbitDef {
  /**
   * Вокруг кого. Луна — планета; планета — звезда (или барицентр у двойной).
   * `null` — барицентр системы.
   */
  parentId: number | null
  /** Радиус орбиты, м, от центра родителя (планеты или барицентра). */
  radius: number
  /** Угол в момент t = 0, рад. */
  phase: number
  /** Угловая скорость, рад/с. Выводится из массы планеты, а не назначается. */
  rate: number
  /** Наклон плоскости орбиты к эклиптике, рад. Ноль — плоская система, а она врёт. */
  tilt: number
}

/** Крупные тела: статичны в масштабах боя, но реальны в координатах. */
export interface BodyEntity {
  id: number
  kind: BodyKind
  /**
   * Чем планета является: скала, лёд, океан. Домен говорит, ЧТО это за мир;
   * во что его покрасить, решает рендер. Раньше он гадал по имени — «есть ли
   * в названии римская цифра» — и новый газовый гигант потребовал бы правки
   * рендера вместо правки данных.
   */
  surface: PlanetType | null
  /**
   * Миллионы жителей. Ноль — мир необитаем.
   *
   * Домену это число нужно для рынка, а рендеру — для огней на ночной стороне.
   * Оба берут его отсюда: «обитаемость» не должна выводиться из имени, цвета или
   * наличия станции, иначе два слоя однажды разойдутся в том, кто где живёт.
   */
  population: number
  /**
   * Кто и как живёт на этом мире: строй, экономика, тех-уровень, раса. `null` —
   * мир необитаем. Это свойство ПЛАНЕТЫ, а не системы: в одной системе уживаются
   * аграрная колония и промышленная столица, у каждой своё. Рынок причала берёт
   * экономику ОТСЮДА, а не одну на всю систему.
   */
  settlement: Settlement | null
  /**
   * Собственное вращение, рад/с. Знак задаёт направление: обратное вращение
   * у планет бывает, и выглядит оно правильно только если знак настоящий.
   *
   * Угол берётся как `spin * world.time`, а не накапливается по кадрам:
   * накопление зависит от частоты кадров и не переживает паузу. Вращение
   * НЕ двигает центр тела, поэтому физика о нём не знает и знать не должна.
   */
  spin: number
  /** Ось вращения в мировых осях. Наклон делает терминатор живым. */
  spinAxis: Vector3
  name: string
  pos: Vector3
  radius: number
  /** Чёрная дыра: масштаб короны/шейдера; гравитация — по `radius`. */
  visualRadius?: number
  color: number
  /** Спутник обращается вокруг планеты. `null` — тело стоит на месте. */
  orbit: OrbitDef | null
}

/**
 * Корабль поколений — «кит». Город размером в километры, идущий сквозь систему
 * тысячелетним рейсом. Это ДЕКОРАЦИЯ, а не участник боя: он не стреляет, его не
 * подбить (лучи и столкновения его не касаются — он не в `ships` и не в `bodies`),
 * и живёт он в своём списке `world.titans`, мимо всей боевой машинерии.
 *
 * Отдельная сущность, а не гигантский `ShipEntity`, потому что у него нет ничего
 * от корабля: ни оружия, ни щита, ни ИИ, ни фракции. Есть лишь путь и облик.
 */
export interface TitanEntity {
  id: number
  kind: 'titan'
  /** Какой из обликов рисовать. Данные, не ветвление: новый вид — строка таблицы. */
  variant: number
  name: string
  pos: Vector3
  /** Дрейф, м/с. Ноль — висит на месте (например, у станции). */
  vel: Vector3
  /** Ориентация: нос вдоль дрейфа, а у висящего — как повернулся при рождении. */
  quat: Quaternion
  /** Медленное продольное вращение, рад/с. Угол берётся как `spin·time`, не копится. */
  spin: number
  /** Габарит для локатора и отсечения. Города-корабли — километры. */
  radius: number
}

/**
 * Пиратская платформа-гнездо. Стационарный «авианосец» упрощённой формы: на
 * палубе дремлет звено пиратов (`crew`), пока гнездо не поднято. В отличие от
 * кита — БОЕВАЯ: у неё есть прочность, её можно расстрелять в металл. Живёт
 * своим списком `world.platforms`, мимо трафика, но её ядро — цель для луча.
 *
 * Столкновений телом не даёт намеренно: сфера радиуса `radius` — только для
 * попаданий, и она меньше выноса палубы, где сидит экипаж, иначе спящих нельзя
 * было бы отстреливать по одному, не задев корпус.
 */
export interface PlatformEntity {
  id: number
  kind: 'platform'
  /** Какой облик рисовать. Данные, не ветвление: новый вид — строка таблицы. */
  variant: number
  name: string
  pos: Vector3
  quat: Quaternion
  /** Радиус сферы попаданий (ядро корпуса), м. Меньше силуэта и выноса палубы. */
  radius: number
  /** Габарит силуэта для локатора и отсечения, м. */
  extent: number
  /** Медленное продольное вращение, рад/с. Угол берётся как spin·time, не копится. */
  spin: number
  hull: number
  maxHull: number
  alive: boolean
  /** Момент взрыва по `world.time`. null — платформа цела. */
  wreckAt: number | null
  /** Гнездо поднято: экипаж проснулся или платформа задета. Обратно не опускается. */
  triggered: boolean
  /** id пиратов на палубе. По ним будим звено и добиваем спящих взрывом платформы. */
  crew: number[]
}

