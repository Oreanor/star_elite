/**
 * Вспышка энергетической бомбы. Позиции у неё НЕТ: поражение мгновенно, а всё,
 * что видит пилот, — экранный эффект на пару секунд. Тела в мире она не образует,
 * ни с чем не пересекается и никого не задевает; урон уже нанесён в `fireBomb`.
 *
 * Радиус тоже не хранится — он выводится из возраста. Хранить то, что следует
 * из времени, значит однажды это рассинхронизировать.
 */
export interface Shockwave {
  born: number
  /** Доля мощности, 0..1. Слабый импульс и светит слабее — иначе вспышка врёт. */
  power: number
}

/**
 * Весть игроку о СОБЫТИИ мира, которое случилось не под его прицелом: знакомый
 * пропал с радара, вероятно погиб. Домен не знает слов — он называет ЧТО и КТО, а
 * текст на языке игрока собирает HUD. Живёт секундами (`CONTACTS.NOTICE_LIFE`) и
 * гаснет сама, как трассер: канал «мир → игрок», а не хранимое состояние.
 */
export interface Notice {
  /** `player-left` — чужой ИГРОК пропал (вышел/перезагрузился): его борт растворился. */
  kind: 'contact-lost' | 'player-left'
  /** Имя, которое игрок знал. Данные, не перевод: подставляется в шаблон HUD. */
  name: string
  /** Момент по `world.time`, от него считается угасание. */
  at: number
}

/**
 * Попадание болта по КИНЕМАТИЧЕСКОМУ (внешне управляемому) борту. Его HP живёт не здесь, а
 * на чужом клиенте, поэтому локально урон НЕ применяется — он лишь РЕГИСТРИРУЕТСЯ, чтобы слой
 * сети переслал его владельцу, а тот ударил по собственному здоровью (авторитет над своим HP).
 *
 * Эфемерный канал «домен → наружу», как трассеры и вести: домен не знает слова «сеть», он
 * только сообщает, какой урон КОСНУЛСЯ бы борта, чьё состояние приходит извне. Слой app
 * забирает список каждый кадр и очищает; в одиночной игре кинематических бортов нет — список пуст.
 */
export interface RemoteHit {
  /** Локальный id кинематического борта, по которому попали. */
  targetId: number
  damage: number
}

/**
 * Вспышка защитного поля станции в точке удара. Чисто визуальный эфемерный эффект (как
 * трассеры/взрывы): станция неуязвима, снаряд гаснет о поле, а домен лишь сообщает, ГДЕ
 * это произошло, чтобы рендер зажёг там голубой кружок. По сети слать не обязательно.
 */
export interface ShieldFlash {
  /** Точка на поле, где погас снаряд. */
  pos: Vector3
  /** Центр станции: рендер ориентирует кружок нормалью вдоль радиуса (лежит на сфере). */
  center: Vector3
  /**
   * Яркость вспышки 0..1. Прямое попадание — единица; отскок корабля тем ярче, чем
   * сильнее удар: вялый упор в поле светится еле-еле (видно, что поле на месте), таран —
   * в полную силу. Рендер домножает на это и цвет, и прозрачность вспышки.
   */
  intensity: number
  born: number
}

export interface World {
  /** Локальное время симуляции, с. Замирает в доке и на паузе. */
  time: number
  /**
   * Общий игровой календарь, с с эпохи (~3000). Обновляет клиент каждый кадр;
   * одинаков у всех игроков. Журналы знакомств штампуются им, не `time`.
   */
  calendarTime: number

  player: ShipEntity
  ships: ShipEntity[]
  asteroids: AsteroidEntity[]
  pods: CargoPodEntity[]
  missiles: MissileEntity[]
  /** Лазерные болты в полёте. Снаряды: летят, заметают отрезок, наносят урон при попадании. */
  bolts: BoltEntity[]
  bodies: BodyEntity[]
  /** Корабли поколений — киты-города. Декорация: мимо боя и столкновений. */
  titans: TitanEntity[]
  /** Пиратские платформы-гнёзда. Боевые: у ядра есть прочность, его можно расстрелять. */
  platforms: PlatformEntity[]

  tracers: Tracer[]
  explosions: Explosion[]
  shockwaves: Shockwave[]
  /** Вспышки гиперпереходов: чужие корабли приходят в систему и уходят из неё. */
  warps: WarpFlash[]
  /** Активные гиперпорталы — приход и уход. */
  warpPortals: WarpPortal[]
  /** Вспышки защитного поля станции: снаряд погас о поле, станция цела. */
  shieldFlashes: ShieldFlash[]

  /** Захваченная цель боя. */
  /** Корабль стоит в доке. Мир при этом не шагает: стыковка — это остановка. */
  docked: boolean
  /**
   * Разрешена ли автоматическая стыковка. Отчаливание выпускает корабль ВНУТРИ
   * зоны причала и на скорости ниже стыковочной, поэтому без этого флага шаг мира
   * стыкует его обратно в том же кадре. Взводится снова, когда корабль покинул зону.
   */
  dockArmed: boolean
  lockedTargetId: number | null
  /**
   * Захваченная СТАНЦИЯ — отдельный класс цели рядом с `lockedTargetId` (борт). Держим врозь
   * намеренно: станцию нельзя атаковать (по ней не наводят оружие/ракету/автобой), с ней можно
   * лишь СВЯЗАТЬСЯ (T → диспетчер). Раздельные поля оставляют задел на настройки листания Tab
   * (только борта / только станции / всё). В любой момент захвачено что-то ОДНО: `cycleLock`
   * ставит одно поле и гасит другое.
   */
  lockedStationId: number | null
  /** Куда летим по навигации. Мост к звёздной карте. */
  navTargetId: number | null
  /**
   * Выбранная на карте галактики система для прыжка, или `null`. Живёт в мире, а
   * не в компоненте карты: прыгать можно только отчалив, поэтому выбор, сделанный
   * у причала, обязан пережить и закрытие карты, и само отчаливание — иначе метку
   * пришлось бы ставить заново уже в полёте. Сбрасывается по прибытии.
   */
  jumpTargetIndex: number | null
  /**
   * Индекс планеты-со-станцией в целевой системе, у которой пилот назначил выход,
   * или `null` — выйти у звезды. Выбор точки прыжка теперь только по причалам, а не
   * в произвольную точку, поэтому хранить достаточно номер планеты. Живёт в мире рядом
   * с `jumpTargetIndex` по той же причине: намеченный у станции выход обязан пережить
   * отчаливание, чтобы прыжок по клавише H в полёте знал, куда именно выходить.
   */
  jumpArrivalPlanet: number | null
  /**
   * Кто СЕЙЧАС занимает причал из трафика (не игрок). Причал один, и стыкуется
   * по нему один корабль за раз: занявший держит место, пока не отчалит, остальные
   * ждут в очереди. null — причал свободен. Игрок стыкуется своим путём (`docked`).
   */
  dockOccupantId: number | null

  /** Секунд до следующей попытки выпустить мирный корабль. Темп трафика — в секундах. */
  trafficTimer: number

  /** Сколько мир уже сдвигали. Истинная позиция = pos + originOffset. */
  originOffset: Vector3
  /**
   * Сдвиг, применённый к миру в последнем `stepWorld`. Ноль, если мир не двигали.
   * Камера обязана прибавить его к своей позиции: она живёт в мировых координатах
   * и иначе остаётся в четырёх километрах позади.
   */
  originShift: Vector3

  /** Единственный источник случайности в симуляции. Math.random() в домене запрещён. */
  rng: Rng
  /** Раздача id. Не глобальный счётчик: на сервере миров будет много. */
  ids: IdSource

  systemName: string
  /** Сфера Дайсона вокруг светила, или `null`. Декорация рендера: облик и цела ли. */
  dyson: DysonSpec | null
  /** Зерно ГАЛАКТИКИ. Все 2500 систем выводятся из него, ничего не хранится. */
  galaxySeed: number
  /** Индекс текущей системы в галактике. Он же вход генератора. */
  systemIndex: number
  /**
   * Счётчик смен системы. Растёт при каждом прыжке.
   *
   * Рендер строит меши тел и пояса один раз при монтировании; узнать, что мир
   * под ним подменили целиком, ему больше неоткуда. Это не «версия для React» —
   * это факт симуляции: миры до и после прыжка не связаны ничем, кроме корабля.
   */
  epoch: number
  credits: number
  score: number

  /**
   * Реестр знакомых пилотов: с кем игрок говорил. Переживает и гибель бортов
   * (пилот — не корабль), и прыжки между системами, как кредиты и очки.
   */
  acquaintances: Acquaintance[]

  /**
   * Вести о событиях вне поля зрения (пропавший знакомый). Домен добавляет, HUD
   * показывает и не трогает — уборка гасит их по возрасту. Эфемерный канал, как трассеры.
   */
  notices: Notice[]

  /**
   * Попадания по кинематическим (чужим) бортам за этот кадр: урон, который НЕ применён
   * локально, а ждёт пересылки владельцу. Слой сети забирает и очищает каждый кадр. В
   * одиночной игре всегда пуст — кинематических бортов нет. Домен цели-«сеть» не знает.
   */
  remoteHits: RemoteHit[]
}

