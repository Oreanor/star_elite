/**
 * Описание системы данными. Новая система — новая запись, а не правка фабрики (OCP).
 *
 * Со временем это будет строиться из StarSystem звёздной карты:
 * `security` даст состав патрулей, `station` — станцию, планеты — тела.
 * Пока задаём вручную.
 */

export interface PatrolDef {
  count: number
  /** Центр появления, мировые координаты (метры). */
  at: readonly [number, number, number]
  /** Разброс появления, м. */
  spread: number
  faction: Faction
  name: string
}

/** Спутник планеты. Орбита — от ЦЕНТРА планеты, метры. */
export interface MoonDef {
  name: string
  radius: number
  orbit: number
  /** Угол в момент t = 0, рад. Разводит луны одной планеты по разным местам. */
  phase: number
  tilt: number
}

/** Вторая звезда двойной системы (из генератора). */
export interface StarCompanionDef {
  kind?: 'star'
  radius: number
  color: number
  separation: number
}

/** Чёрная дыра — спутник барицентра вместо звезды B. */
export interface BlackHoleCompanionDef {
  kind: 'blackhole'
  name: string
  /** Шварцшild / гравитация, м — компактнее звезды. */
  radius: number
  /** Масштаб короны, диска и линзы — как у звезды B до замены. */
  visualRadius: number
  separation: number
  diskAxis?: readonly [number, number, number]
}

/** Отдельная тестовая чёрная дыра, привязанная смещением к станции. */
export interface BlackHoleDef {
  name: string
  radius: number
  stationOffset: readonly [number, number, number]
  diskAxis?: readonly [number, number, number]
}

export interface SystemDef {
  name: string
  /** Одно число задаёт всю расстановку: пояс, патрули, разброс. */
  seed: number
  playerStart: readonly [number, number, number]
  star: { pos: readonly [number, number, number]; radius: number; color: number }
  /**
   * Спутник двойной: вторая звезда или чёрная дыра (барицентр, `separation`, орбита).
   * Позиции нет — место даёт время, как у звёзд пары.
   */
  companion: StarCompanionDef | BlackHoleCompanionDef | null
  /** Отдельные чёрные дыры; не заменяют звёзды и не участвуют в барицентре пары. */
  blackHoles?: readonly BlackHoleDef[]
  /** Сфера Дайсона вокруг светила, или `null`. Только декорация: облик и цела ли. */
  dyson: DysonSpec | null
  planets: readonly {
    name: string
    type: PlanetType
    pos: readonly [number, number, number]
    radius: number
    color: number
    /** Собственное вращение, рад/с. Отрицательное — обратное. */
    spin: number
    /** Наклон оси, рад. Ноль — ось строго вертикальна. */
    tilt: number
    /** Миллионы жителей. Ноль — мир необитаем, и ночью на нём темно. */
    population: number
    /**
     * Кто и как живёт: строй, экономика, тех, раса. `null`/отсутствует — необитаем.
     * Своё у КАЖДОЙ планеты: рынок причала и характер мира берутся отсюда.
     */
    settlement?: Settlement | null
    /** Спутники. Их период выводится из массы планеты, поэтому здесь его нет. */
    moons: readonly MoonDef[]
  }[]
  station: { name: string; pos: readonly [number, number, number]; radius: number } | null
  belt: { center: readonly [number, number, number]; radius: number; count: number } | null
  patrols: readonly PatrolDef[]
}

