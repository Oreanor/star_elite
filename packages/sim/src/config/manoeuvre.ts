/**
 * Фигуры пилотажа.
 *
 * Каждая фигура меряется УГЛОМ, а не секундами. По времени бочка при крене
 * 3.08 рад/с успевала провернуть 220° и замирала вверх ногами. Оборот,
 * отмеренный углом, кончается ровно там, где начался, при любых маневровых.
 */
export const MANOEUVRE = {
  HALF_TURN: Math.PI,
  FULL_TURN: Math.PI * 2,

  /**
   * Предохранитель по времени, с. Петля идёт вокруг тангажа, а он самая
   * медленная ось: 2π при 1.33 рад/с — почти пять секунд. Бочке хватило бы 3.5,
   * но общий потолок проще одного на фигуру и ничего не портит: он аварийный.
   */
  MAX_DURATION: 9,

  /**
   * Пауза между фигурами, с — но только для заказа ИЗ ПРОСТОЯ. Заказанная ещё во
   * время фигуры следующая идёт вплотную, без паузы (буфер на один ввод): связку
   * «влево-вправо» не должно рвать искусственной задержкой. Пауза же короткая,
   * чтобы и без буфера крутить можно было бодро, но не мгновенным спамом.
   */
  COOLDOWN: 0.5,

  /**
   * Радиус петли, м. Не тяга и не «доля газа»: у петли есть ровно один размер,
   * и он считается, а не подбирается.
   *
   * Корабль на петле идёт по кругу радиусом v/ω, значит нужная скорость — ω·R.
   * Держать вместо этого рукоять газа нельзя: она в момент двойного нажатия W
   * стоит уже на единице (W её и поднимает), круг распухает до полукилометра,
   * и корабль улетает вперёд вместо того, чтобы пропустить преследователя.
   */
  LOOP_RADIUS: 110,

  /**
   * Бочка — короткий форсаж маневровых, а не обычный крен. Ручку крена она
   * «переваливает за упор» (значение больше единицы), поэтому корабль крутится
   * резко, а не переваливается лениво. Оборот всё равно отмеряется фактической
   * угловой скоростью и выходит ровно один — быстрее, но не длиннее.
   */
  BARREL_ROLL_STICK: 2.6,
  /**
   * Плавный выход из бочки. Вторую половину оборота — БЕЗ заказанной следующей —
   * корабль тормозит встречным креном и мягко встаёт у ровного, а не клинит колом
   * на полном ходу. Тормозим именно с СЕРЕДИНЫ, а не в последние градусы: угловое
   * ускорение мало (≈5 рад/с²), и остановка с полного хода занимает почти весь
   * оборот — в последние 20–30° сменить скорость просто нечем, стоп остался бы
   * резким.
   *
   * Успел за это время нажать бочку снова (буфер) — торможения нет: добираем оборот
   * на полном ходу и уходим в следующую вплотную.
   */
  BARREL_BRAKE_STOP: 0.35,
  /**
   * Разворот — тоже форсажем: ручку тангажа и крена переваливает за упор, оттого
   * иммельман крутится вдвое резче. Тяга петли считается от фактической угловой
   * скорости (loopThrottle), поэтому радиус держится сам — быстрее, но не шире.
   */
  REVERSAL_STICK: 3.3,
  /**
   * Петля — вокруг тангажа, самой медленной оси. Раньше её крутили ровно на упоре
   * (доля 1), и полный оборот тянулся впятеро дольше бочки — фигура читалась вялой.
   * Форсаж и ей: доля больше единицы задирает угловую скорость выше паспортной,
   * оборот идёт резче. Радиус держит loopThrottle от фактической ω — быстрее, но
   * не шире: на возросшую ω он просто просит пропорционально больше хода.
   */
  LOOP_STICK: 1.5,
  /**
   * Тяга уклонения в бочке — тем же форсажем. Смещение с линии ∝ 1/ω², поэтому
   * на быстром обороте корабль перестал бы сходить с линии, и бочка потеряла бы
   * смысл уклонения. Повышенная боковая тяга возвращает сход, укладывая его в
   * короткий резкий оборот.
   */
  BARREL_STRAFE_STICK: 3.2,

  /**
   * Доля паспортной скорости, ниже которой петлю не начинают, 0..1.
   *
   * Радиус выводится из угловой скорости (v = ω·R), а она в начале фигуры равна
   * нулю: маневровые раскручиваются не мгновенно. Голая формула поэтому просила
   * НУЛЕВУЮ тягу, лётный компьютер честно гасил ход, и петля вырождалась в
   * кувыркание на месте. Пол не даёт скорости провалиться, пока тангаж набирает
   * обороты: круг в начале шире расчётного, зато это круг, а не пируэт.
   */
  LOOP_MIN_THROTTLE: 0.45,
} as const
