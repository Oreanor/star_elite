/**
 * Миелофон — артефакт непрерывного масштаба: борт растёт от обычного размера к
 * системному и дальше. Пока коллизии не отменены (до звёздного размера), гигант —
 * это твёрдое тело: давит мелочь, а сам почти цел (урон делится на масштаб). Здесь
 * только числа — темп роста и границы; правила живут в `domain/scale`.
 */
export const MIELOPHONE = {
  /** Нижняя граница — обычный корабль. */
  MIN_SCALE: 1,
  /**
   * Верхняя граница — практически безгранично (1e15): цель фичи расти до галактического
   * масштаба. Не `Infinity` намеренно: масса растёт кубом (scale³), и `Infinity` дал бы
   * NaN в дележе импульса при столкновении. 1e15 не достижимо руками (≈30 с удержания).
   * Тряска у самого верха — потеря точности float в матрицах вида; её снимет рефрейминг
   * координат (мир ÷ scale вокруг корабля), тогда и путь к галактикам станет стабильным.
   */
  MAX_SCALE: 1e15,
  /**
   * Темп роста. Масштаб множится на e^(GROW_RATE·dt) при полном сигнале — рост
   * ЭКСПОНЕНЦИАЛЬНЫЙ, потому что «зум» воспринимается кратно: постоянный сигнал даёт
   * постоянную скорость увеличения на глаз. e^1.2 ≈ утроение размера за секунду.
   */
  GROW_RATE: 1.2,
  /**
   * Ниже этого масштаба корабль сквозной, как и был: обычные корабли друг о друга не
   * бьются. Столкновение включается только там, где появился гигант, — иначе бой из
   * маневров превратился бы в бильярд.
   */
  COLLIDE_MIN_SCALE: 1.5,
} as const
