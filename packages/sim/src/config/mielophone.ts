/**
 * Миелофон — артефакт непрерывного масштаба: борт растёт от обычного размера к
 * системному и дальше. Пока коллизии не отменены (до звёздного размера), гигант —
 * При scale>1 твердь только у Shift+Tab-навигации (планеты, станции, глыбы…);
 * борта и мелочь пояса сквозные. Здесь числа — темп роста и границы; правила в domain.
 */
export const MIELOPHONE = {
  /** Нижняя граница — обычный корабль. */
  MIN_SCALE: 1,
  /**
   * Верхняя граница роста (зум). Дальше расти некуда. Не `Infinity`: масса растёт кубом
   * (scale³), и `Infinity` дал бы NaN в дележе импульса. Согласовано с
   * GALAXY_LAYER.LOCK_SCALE в render/config.
   */
  MAX_SCALE: 1e8,
  /**
   * Темп роста. Масштаб множится на e^(GROW_RATE·dt) при полном сигнале — рост
   * ЭКСПОНЕНЦИАЛЬНЫЙ, потому что «зум» воспринимается кратно: постоянный сигнал даёт
   * постоянную скорость увеличения на глаз. e^1.2 ≈ утроение размера за секунду.
   */
  GROW_RATE: 1.2,
  /**
   * Рост ПИТАЕТСЯ от батареи доп-отсека (аукс): миелофон — тоже аукс-устройство. Полного
   * заряда хватает вырасти примерно в GROW_FULL_FACTOR раз; дальше — жди подзарядки. Расход
   * считается по ЛОГАРИФМУ роста (масштаб множится), поэтому «в 1000 раз» — это ln(1000)
   * логарифмических единиц на полную батарею. Сжатие (обратно) бесплатно. Число тюнится.
   */
  GROW_FULL_FACTOR: 1000,
  /**
   * Фазовый переход «мелкий мир ↔ крупный». С PHASE_START борт начинает РАСТВОРЯТЬСЯ для
   * единичного мира (и мелочь — для него), к PHASE_END растворяется полностью: пропадает и
   * с чужих экранов, и из взаимодействий. Симметрично: за PHASE_END гигант игнорирует
   * мелочь (корабли, обломки), оставляя КРУПНОЕ — планеты, станции, китов, звёзды. Это и
   * есть плавный уход в «большой мир» (задел под галактический масштаб).
   *
   * Порог визуального рендера привязан сюда же (км-корпус за ним мерцает в лог-буфере, а
   * его к тому времени и не видно). Растёшь дальше без предела — просто в другом мире.
   */
  PHASE_START: 1000,
  PHASE_END: 2000,
  /**
   * Выше — крупная твердь сквозная + галактический слой в кадре (шары/локатор/J к звезде).
   * Единственный порог «система → галактика»: рендер читает его как FADE_IN / BODY_FADE / SKY.
   * При 1 < scale < GHOST_BODY твердь только у Shift+Tab (планеты/станции/глыбы…).
   */
  GHOST_BODY_SCALE: 5_000,
  /**
   * Опорный масштаб для скорости/тяги. Множитель = max(1, scale / SPEED_SCALE_REF).
   * REF = 1: корпус в N раз крупнее → потолок и тяга в N раз выше (постоянные «длины
   * корпуса в секунду»). На миллионах × система проходится газом; к пробуждению
   * галактического слоя соседние звёзды — тоже без обязательного крейсера.
   */
  SPEED_SCALE_REF: 1,
  /**
   * Показатель кривой таяния (ease-in): opacity = 1 − t^FADE_EXP, t∈[0,1] по фазе.
   * >1 держит объект плотным почти до конца и гасит рывком в финале — так «призраков»
   * (полупрозрачных) в кадре мало, а не размазано по всему диапазону.
   */
  FADE_EXP: 3,
} as const
