/**
 * Тяготение крупных тел. Нужно оно ровно в одном месте — чтобы посчитать, с
 * какой скоростью обращается луна, — но посчитать, а не назначить.
 *
 * Назначенный период («пусть оборот за десять минут») даёт луне орбитальную
 * скорость в сотни километров в секунду: она проносится мимо корабля быстрее
 * ракеты и сшибает его на ровном месте. Настоящая Луна идёт километр в секунду,
 * и это следствие массы Земли, а не решения дизайнера.
 */
/**
 * Что бывает крупным телом. Список живёт в конфиге, а не в сущности, потому что
 * по нему приходится строить таблицы («зона торможения у каждого своя»), а
 * таблица обязана ломаться компиляцией, если завтра появится комета.
 */
export const BODY_KINDS = ['star', 'planet', 'moon', 'station'] as const
export type BodyKind = (typeof BODY_KINDS)[number]

export const GRAVITY = {
  /** Гравитационная постоянная, м³·кг⁻¹·с⁻². */
  G: 6.674e-11,

  /**
   * Плотность мира, кг/м³. Масса выводится из радиуса: `ρ·(4/3)πR³`.
   *
   * Числа солнечные, и проверить их легко: с ними луна Оссиании обходит её за
   * 29 суток (у настоящей Луны 27.3), а спутники газового гиганта — за 1.8 и
   * 7.1 (у Ио 1.77, у Ганимеда 7.15). Не подгонка: у скалистых миров плотность
   * и правда около пяти тысяч, у Юпитера — 1330.
   */
  ROCK_DENSITY: 5000,
  GAS_DENSITY: 1330,
  /**
   * Плотность звезды, кг/м³. Солнечная — 1408. Нужна там же, где и остальные:
   * чтобы посчитать период двойной, а не назначить его. Две звезды в паре
   * обходят общий центр за ω = √(G·(M₁+M₂)/d³), и масса каждой выводится из
   * радиуса — ровно как масса планеты у луны.
   */
  STAR_DENSITY: 1408,
} as const

