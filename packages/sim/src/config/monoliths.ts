/**
 * Монолиты — исполинские статуи у причала.
 *
 * Чистая декорация масштаба, как и киты: висят, ничего не делают, ни с чем не сталкиваются.
 * Смысл их — дать глазу мерило. Корабль в 26 метров рядом с десятикилометровой статуей
 * читается букашкой, и система перестаёт быть пустым чёрным полем.
 */
export const MONOLITH = {
  /** Сколько обликов рисует рендер. Расстановка кладёт по одному каждого. */
  VARIANTS: 3,

  /** Габарит, м. «По десять километров каждая» — радиус вдвое меньше поперечника. */
  RADIUS: 5_000,

  /**
   * Кувырок, рад/с. МЕДЛЕННЫЙ — вчетверо тише станции (у той 0.08): статуя не механизм,
   * ей крутиться незачем. Полный оборот выходит примерно за пять минут — движение заметно,
   * только если задержать взгляд, и это ровно то ощущение, которое нужно.
   */
  SPIN: 0.02,

  /**
   * На каком удалении от причала стоят, м — кратно своему радиусу. Достаточно близко, чтобы
   * попадать в кадр вместе со станцией, и достаточно далеко, чтобы не загораживать её.
   */
  STATION_GAP_MIN: 2.5,
  STATION_GAP_MAX: 4.0,
} as const
