/**
 * Нагрев корпуса близкой звездой.
 *
 * Подойти к светилу можно — заглянуть, зачерпнуть топливо, — но не прижаться:
 * у короны корпус раскаляется, и, если не отойти, начинает течь сперва щит,
 * потом обшивка. Защита тут одна — манёвр: нагрев падает сам, стоит отдалиться.
 * Никакого «отсидеться под щитом»: щит корону не держит, он лишь сгорает первым.
 *
 * Зона задана в ДОЛЯХ РАДИУСА звезды, а не в метрах: у красного карлика в 700
 * тыс. км и у голубого гиганта в 2400 корона своя, и опасная высота обязана
 * быть своей у каждого. Метр от поверхности одинаково смертелен у обоих.
 */
export const STAR_HEAT = {
  /**
   * Высота над поверхностью (в радиусах звезды), НИЖЕ которой нагрев максимален.
   * 0.2 радиуса — это уже внутри видимой короны: подходить настолько близко можно
   * лишь нарочно.
   */
  DANGER_RATIO: 0.2,
  /** Высота (в радиусах), ВЫШЕ которой звезда не греет вовсе. Между ними — плавно. */
  SAFE_RATIO: 1.2,

  /**
   * Скорость набора и спада нагрева, с⁻¹ (экспоненциальное приближение к цели).
   * Набор медленнее спада: у короны корпус калится за несколько секунд, а
   * остывает, стоит отойти, ещё быстрее — побег обязан работать сразу.
   */
  RISE_RATE: 0.25,
  COOL_RATE: 0.45,

  /**
   * Пороги нагрева. Ниже WARN — просто греешься. WARN..CRITICAL — жёлтый пуш «ПЕРЕГРЕВ»
   * (пора отворачивать). CRITICAL..DESTROY — «КРИТИЧЕСКИЙ ПЕРЕГРЕВ» (последнее окно). На
   * DESTROY корпус разрушается МГНОВЕННО: у звезды нет «отсидеться под щитом», только манёвр.
   * Нагрев тянется к облучённости экспонентой, так что от CRITICAL до DESTROY — секунды,
   * которых хватает уйти, если реагировать на пуш.
   */
  WARN: 0.5,
  CRITICAL: 0.75,
  DESTROY: 0.99,
} as const

