/**
 * Игровой календарь. Копия земного, сдвинутая к ~3000 и сжатая: даты на станциях и
 * HUD идут, мир дышит (контракты протухают, знакомые дрейфуют), но пилот по этому
 * счёту НЕ стареет — возраст, если понадобится, пойдёт по своим часам.
 *
 * Время — величина РАЗДЕЛЯЕМАЯ: в общем мире у всех одни часы, иначе даты разъедутся.
 * Календарь считается от `TIME.ANCHOR_REAL_MS` по реальным UTC-часам (онлайн — через
 * смещение Firebase Server Time). Домен хранит `world.calendarTime` — его обновляет
 * клиент; `world.time` — локальные секунды симуляции (пауза в доке, физика).
 */
export const TIME = {
  /**
   * Эпоха: игровой момент на старте отсчёта, мс от Unix. Полночь 1 января 3000 UTC.
   * `Date.UTC` — чистая константа (не чтение часов), поэтому здесь ей место.
   */
  EPOCH_MS: Date.UTC(3000, 0, 1),
  /**
   * Реальный UTC-момент, когда игровой календарь был на нуле (совпадает с EPOCH_MS).
   * Один для всех клиентов: от него считается общее время, офлайн и онлайн.
   */
  ANCHOR_REAL_MS: Date.UTC(2026, 0, 1),
  /**
   * Сжатие: во сколько раз игровой календарь быстрее реального. 480 — одна реальная
   * минута ≈ восемь игровых часов, сутки проходят за три минуты игры. Единственный
   * рычаг темпа: год листается правкой этого числа.
   */
  SCALE: 480,
} as const

/** Игровой календарь, мс (сдвинутый к ~3000), от реального UTC-момента. */
export function calendarMs(realMs: number): number {
  return TIME.EPOCH_MS + (realMs - TIME.ANCHOR_REAL_MS) * TIME.SCALE
}

/**
 * Секунды с эпохи в «реальном» темпе якоря: одна реальная секунда = +1.
 * Журналы и `world.calendarTime` хранят это; даты в UI — `calendarMs` или `at × SCALE`.
 */
export function calendarSec(realMs: number): number {
  return (calendarMs(realMs) - TIME.EPOCH_MS) / (1000 * TIME.SCALE)
}

/** Секунды игрового календаря для орбит и неба: `calendarTime × SCALE`. */
export function orbitSec(calendarTime: number): number {
  return calendarTime * TIME.SCALE
}
