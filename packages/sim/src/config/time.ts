/**
 * Игровой календарь. Копия земного, сдвинутая к ~3000 и сжатая: даты на станциях и
 * HUD идут, мир дышит (контракты протухают, знакомые дрейфуют), но пилот по этому
 * счёту НЕ стареет — возраст, если понадобится, пойдёт по своим часам.
 *
 * Время — величина РАЗДЕЛЯЕМАЯ: в общем мире у всех одни часы, иначе даты разъедутся.
 * Оттого источник один — сервер (когда появится): он ведёт эпоху и текущий момент, а
 * клиент лишь показывает. Пока сервера нет, клиент считает сам от старта — но так, что
 * серверу останется подменить только точку отсчёта. Здесь — числа, без часов реального
 * мира: `Date.now` живёт в клиенте (`ui/clock`), домен его не касается.
 */
export const TIME = {
  /**
   * Эпоха: игровой момент на старте отсчёта, мс от Unix. Полночь 1 января 3000 UTC.
   * `Date.UTC` — чистая константа (не чтение часов), поэтому здесь ей место.
   */
  EPOCH_MS: Date.UTC(3000, 0, 1),
  /**
   * Сжатие: во сколько раз игровой календарь быстрее реального. 240 — одна реальная
   * минута ≈ четыре игровых часа, сутки проходят за шесть минут игры. Единственный
   * рычаг темпа: год листается правкой этого числа.
   */
  SCALE: 240,
} as const
