  /**
   * Массовая блокировка: чужой корабль ближе этого — крейсер не включается.
   * Без неё любой бой заканчивается мгновенным побегом, и боя нет вообще.
   */
  MASS_LOCK_RANGE: 900,

  /**
   * Крейсер начинает штатный спад немного раньше внешней границы гравитации звезды.
   * Эта добавка к радиусу даёт ненулевой запас даже почти остановившемуся кораблю;
   * основной тормозной путь считается из его фактической скорости.
   */
  STAR_EXIT_BUFFER_RADII: 0.05,

  /** Выше этого множителя корабль «не в фазе»: не стреляет и не сталкивается. */
  PHASE_THRESHOLD: 1.5,

  /** Ниже этого считаем, что крейсер выключен (числовой шум). */
