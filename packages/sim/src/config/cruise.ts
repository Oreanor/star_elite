import type { BodyKind } from './bodies'

/**
 * Крейсерский ход. Держишь клавишу — скорость растёт, отпустил — падает.
 *
 * Он существует не для красоты: до планеты 500 км, до звезды 150 миллионов.
 * На боевых 220 м/с первый перелёт занял бы сорок минут, второй — двадцать
 * шесть лет. Система настоящего масштаба без крейсера непроходима в принципе.
 *
 * Поэтому крейсер СВЕРХСВЕТОВОЙ, как суперкруиз в Elite Dangerous: 220 м/с ×
 * 40 000 000 ≈ 8.8·10⁹ м/с, то есть 29 c. Одна а.е. проходится за семнадцать
 * секунд, до газового гиганта в 4.5 а.е. — минута с небольшим.
 */
export const CRUISE = {
  /**
   * Во сколько раз крейсер превосходит боевую скорость на полном ходу.
   * Достижим только в межпланетной пустоте: рядом с телом потолок ниже (см. BRAKE_ZONE).
   */
  MAX_FACTOR: 40_000_000,

  /**
   * Скорость экспоненциального РОСТА множителя, с⁻¹: factor *= exp(k·dt).
   * Постоянное время удвоения ⇒ до полного хода ln(4·10⁷)/1.2 ≈ 15 с.
   */
  CHARGE_RATE: 1.2,
  /** Спад, с⁻¹. Быстрее разгона: сбросить ход всегда легче, чем набрать. */
  DECAY_RATE: 1.6,

  /**
   * Массовая блокировка: чужой корабль ближе этого — крейсер не включается.
   * Без неё любой бой заканчивается мгновенным побегом, и боя нет вообще.
   */
  MASS_LOCK_RANGE: 900,

  /**
   * Высота над поверхностью ближайшего тела, дающая ровно один боевой ход:
   * предельный множитель = высота / BRAKE_ZONE. Скорость крейсера, стало быть,
   * пропорциональна расстоянию до поверхности — ровно как суперкруиз в E:D.
   *
   * Раньше зона задавалась как «столько-то радиусов тела» и зажималась сверху.
   * При настоящих размерах это перестало работать: радиусы тел различаются
   * в сто раз, а расстояния между ними — в двадцать тысяч. Любой множитель на
   * радиус упирался в потолок, и потолок оставался единственным, что решало.
   * Высота уже содержит радиус в себе (это `расстояние − радиус`), поэтому
   * честнее одна константа, чем три, две из которых ни на что не влияют.
   *
   * Следствие приятное и бесплатное: торможение получается само. У причала
   * (высота 1.6 км) множитель равен единице — не разогнаться. Подлетая к планете,
   * теряешь высоту, вместе с ней потолок, и корабль гасит ход по экспоненте,
   * никогда не достигая поверхности.
   *
   * Число задаёт ВРЕМЯ ВЗЛЁТА, и это неочевидно. Скорость равна `MAX_SPEED·h/BRAKE_ZONE`,
   * а растёт от неё сама высота: `dh/dt = MAX_SPEED·h/BRAKE_ZONE`. Высота, стало быть,
   * умножается на `e` каждые `BRAKE_ZONE/MAX_SPEED` секунд, и весь путь от причала
   * до звезды — это восемнадцать таких удвоений, сколько ни жми.
   *
   * При 4000 м период был 18 с, и `scratch/travel.ts` намерил: до звезды в одной
   * а.е. — больше десяти минут, из них первые ЧЕТЫРЕ корабль почти стоит на месте,
   * а планета за спиной не сдвигается. Это читается как поломка, а не как масштаб.
   *
   * 1200 м дают период 5.5 с: до звезды 4 минуты, до причала с четырёхсот
   * километров — минута. Ни одно другое число трогать не пришлось.
   *
   * Зона своя у каждого вида тела, и это ТАБЛИЦА, а не ветвление: комета завтра
   * добавится строкой. Луне дано вчетверо меньше — 1.4 секунды до касания вместо
   * 5.5. Причина не в снисхождении, а в замере (`scratch/moonload.ts`): потолок
   * берётся у БЛИЖАЙШЕГО тела, поэтому луна режет ход только внутри пузыря, где
   * она ближе своей планеты, — а пузырь целиком лежит внутри пузыря планеты.
   * С общей зоной луны обходились в 1% времени полёта к звезде (253 с против 250),
   * то есть не мешали вовсе — но подлетать к камню в 1700 км с той же осторожностью,
   * что к звезде в 700 000, незачем.
   */
  BRAKE_ZONE: {
    star: 1_200,
    planet: 1_200,
    moon: 300,
    /** Причал: у него зона равна планетной, иначе к нему подходишь на километре в секунду. */
    station: 1_200,
  } as Record<BodyKind, number>,

  /** Выше этого множителя корабль «не в фазе»: не стреляет и не сталкивается. */
  PHASE_THRESHOLD: 1.5,

  /** Ниже этого считаем, что крейсер выключен (числовой шум). */
  IDLE_EPSILON: 1.02,
} as const
